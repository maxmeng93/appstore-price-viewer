/** 按分组获取地区 */
export function getRegionsByGroup(): Record<RegionGroup, Region[]> {
  const grouped: Record<RegionGroup, Region[]> = {
    "asia-pacific": [],
    europe: [],
    americas: [],
    "middle-east-africa": [],
  };
  for (const region of REGIONS) {
    grouped[region.group].push(region);
  }
  return grouped;
}

/** 根据代码查找地区 */
export function getRegionByCode(code: string): Region | undefined {
  return REGIONS.find((r) => r.code === code);
}

/** 根据 locale 获取地区显示名称 */
export function getRegionName(region: Region, locale: Locale): string {
  switch (locale) {
    case "zh": return region.name;
    case "en": return region.nameEn;
    case "ja": return region.nameJa;
    case "ko": return region.nameKo;
    case "ru": return region.nameEn;
  }
}
