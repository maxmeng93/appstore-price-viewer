/** 地理分组类型 */
export type RegionGroup =
  | "asia-pacific"
  | "europe"
  | "americas"
  | "middle-east-africa";

/** 分组中文名映射 */
export const REGION_GROUP_NAMES: Record<RegionGroup, string> = {
  "asia-pacific": "亚太地区",
  europe: "欧洲",
  americas: "美洲",
  "middle-east-africa": "中东与非洲",
};

/** Apple App Store 支持的国家/地区及其货币 */
export interface Region {
  code: string; // ISO 两位国家代码
  name: string; // 中文名称
  nameEn: string; // 英文名称
  nameJa: string; // 日文名称
  nameKo: string; // 韩文名称
  nameOriginal: string; // Apple 页面原文名称
  currency: string; // 货币代码
  flag: string; // emoji 旗帜
  group: RegionGroup; // 地理分组
  appleUrl: string; // Apple 链接
}

/** 根据 ISO 两位国家代码生成 emoji 旗帜 */
function flag(code: string): string {
  return String.fromCodePoint(
    ...code
      .toUpperCase()
      .split("")
      .map((c) => 0x1f1e6 + c.charCodeAt(0) - 65)
  );
}

